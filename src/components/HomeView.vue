        'tye-2021': {
          id: null,
          title: 'Tye Ruotolo - Champion Pan Américain 2021',
          query: 'Tye Ruotolo Pan American 2021 final',
          loaded: false
        }
      },
      loading: false,
      error: null
    }
  },
  async created() {
    // Charger les vidéos de manière asynchrone
    this.loadVideosAsync()
  },
  methods: {
    async loadVideosAsync() {
      this.loading = true
      this.error = null

      try {
        // Charger les vidéos une par une de manière asynchrone
        const loadPromises = Object.entries(this.videos).map(async ([key, video]) => {
          try {
            const results = await youtubeService.searchVideos(video.query, 1)
            if (results && results.length > 0) {
              this.videos[key].id = results[0].id.videoId
              const videoDetails = await youtubeService.getVideoDetails(results[0].id.videoId)
              if (videoDetails) {
                this.videos[key].title = videoDetails.snippet.title
              }
              this.videos[key].loaded = true
            }
          } catch (error) {
            console.error(`Erreur lors du chargement de la vidéo ${key}:`, error)
          }
        })

        // Attendre que toutes les vidéos soient chargées
        await Promise.all(loadPromises)
      } catch (error) {
        console.error('Erreur lors du chargement des vidéos:', error)
        this.error = 'Erreur lors du chargement des vidéos'
      } finally {
        this.loading = false
      }
    },
    showVideo(videoKey) {
      const video = this.videos[videoKey]
      if (video && video.id) {
        this.selectedVideo = video.id
        this.videoTitle = video.title
      }
    },
    closeVideo() {
      this.selectedVideo = null
      this.videoTitle = ''
    }
  }
}
</script> 