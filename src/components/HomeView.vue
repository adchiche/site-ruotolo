    return {
      selectedVideo: null,
      videoTitle: '',
      videos: {
        'kade-2023': {
          id: null,
          title: 'Kade Ruotolo - Champion du Monde ADCC 2023',
          query: 'Kade Ruotolo ADCC 2023 final',
          loaded: false
        },
        'kade-2022': {
          id: null,
          title: 'Kade Ruotolo - Champion du Monde IBJJF 2022',
          query: 'Kade Ruotolo IBJJF 2022 final',
